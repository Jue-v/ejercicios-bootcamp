package com.example;

public class TiposVar {

    public static void main(String[] args) {

        //Numericos
        System.out.println( "Tipos numericos ");
        byte variable = 5;
        System.out.println(variable);
        short variable1 = 10;
        System.out.println( variable1);
        int variable2 = 30;
        System.out.println( variable2);
        long variable3 = 40;
        System.out.println( variable3);

        float variable4 = 7.2f;
        System.out.println( variable4);
        double variable5 = 100.0d;      //Mayor rango
        System.out.println( variable5);

        //Booleano
        System.out.println( "Tipo booleano");
        boolean variable6 = true;
        System.out.println( variable6);
        boolean variable7 = false;
        System.out.println( variable7);

        //Texto
        System.out.println( "Tipo textos");
        char texto = 'a';
        System.out.println( texto);
        String texto1 = "Hola mundo";
        System.out.println( texto1);


    }
}
